public class MixedMath {
  public static void main(String[] args)
  {
    int num;
    int num2;
    int num3;
    int num4;
    int num5;
    int num6;
    char c = 'c';
    char a = 'a';
    char r = 'r';
    char l = 'l';
    char o = 'o';
    char s = 's';
    num = c;
    num2 = a;
    num3 = r;
    num4 = l;
    num5 = o;
    num6 = s;
    System.out.println("ASCII for carlos = " + num + num2 + num3 + num4 + num5 + num6);
  }
}
